//! 网格与材质基础抽象（v0）
//!
//! 设计目标：
//! - 提供一个极简的网格与实例数据封装，供示例和后续渲染系统复用
//! - 不绑定具体渲染管线，只负责描述「网格数据长什么样」
//!
//! 注意：按照 CI 规范，这里只放类型与 trait 声明，不放具体实现细节。

/// 二维网格：只包含一个顶点缓冲。
///
/// 约定：
/// - 顶点布局由渲染管线约定（例如 location(0) = vec2<f32> 位置）
/// - `vertex_count` 表示可以绘制的顶点数量
pub struct Mesh2D {
    /// 顶点缓冲
    pub vertex: wgpu::Buffer,
    /// 顶点数量
    pub vertex_count: u32,
}

/// 二维实例数据批次：用于一次绘制多个实例。
///
/// 约定：
/// - 实例布局由渲染管线约定（例如位置偏移 + 颜色等）
/// - `count` 表示实例数量
pub struct InstanceBatch2D {
    /// 实例缓冲
    pub buffer: wgpu::Buffer,
    /// 实例数量
    pub count: u32,
}

